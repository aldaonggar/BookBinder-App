namespace App\Repository;

use App\Entity\Book;
use App\Entity\Rating;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @extends ServiceEntityRepository<Rating>
 *
 * @method Rating|null find($id, $lockMode = null, $lockVersion = null)
 * @method Rating|null findOneBy(array $criteria, array $orderBy = null)
 * @method Rating[]    findAll()
 * @method Rating[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class RatingRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Rating::class);
    }

    public function save(Rating $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Rating $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function getAverageRating(Book $book): int
    {
        $bookId = $book->getId();

        $qb = $this->createQueryBuilder('r')
            ->where('r.book == :book')
            ->setParameter('book', $bookId);

        $query = $qb->getQuery();

        $ratings = array($query->execute());

        $length = count($ratings);
        $sumRating = 0;
        for ($i = 0; $i < $length; $i ++){
            $sumRating += $ratings[$i]->getScore();
        }

        $avRating = $sumRating/$length;
        return $avRating;
    }

